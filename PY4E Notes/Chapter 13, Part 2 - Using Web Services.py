# XML Schema
# Description of legal format of an XML document
# Expressed in terms of constraints on the structure and content of the documents
# Often used to specify a "contract" between systems - "My system will only accept XML that
# conforms to this particular Schema."
# If a particular piece of XML meets the specification of the Schema - it is said to "validate"

# XSD Structure
# Includes Complex Elements, Simple Elements, and Sequences

# XSD Constraints
# minOccurs: The minimum number of elements you can have of a certain type in a list
# maxOccurs: The minimum number of elements you can have of a certain type in a list

# XSD Data Types
# type = "xs:string", "xs:date" (2001-01-01), "xs:dateTime" (2001-01-01T09:30:10Z)<-Z indicates a time zone,
# "xs:decimal", "xs:integer"
