# Relational Databases
# Relational databases model data by storing rows and columns in tables.
# Relational databases are able to efficiently retrieve data from the tables.

# Database - contains many tables
# Relation (or table) - contains tuples and attributes
# Tuple (or row) - a set of fields that generally represents an "object" like a person or a music track
# Attribute (or column) - one of many elements of data corresponding to the object represented by the row

# SQL
# Structured Query Language is the language we use to issue commands do the database